var numero = 1;

switch (numero) {
    case 1:
        console.log("Soy uno!"); // Imprime "Soy uno!" en la consola
        break; // La instrucción "break" indica que se debe salir del switch después de ejecutar este caso.

    case 10:
        console.log("Soy diez!"); // No se ejecuta en este ejemplo
        break;

    case 100:
        console.log("Soy cien!"); // No se ejecuta en este ejemplo
        break;

    default:
        console.log("No soy nada!"); // No se ejecuta en este ejemplo
        break;
}
